CREATE OR REPLACE PACKAGE PMP_CP0025 IS

-- 予定情報配列定義
TYPE IX_A_APPOINTMENT_ID          IS TABLE OF PMT_APPOINTMENT.APPOINTMENT_ID%TYPE             INDEX BY BINARY_INTEGER;  -- 予定ID
TYPE IX_A_APPOINTMENT_TYPE        IS TABLE OF PMT_APPOINTMENT.APPOINTMENT_TYPE%TYPE           INDEX BY BINARY_INTEGER;  -- 予定種類
TYPE IX_A_APPOINTMENT_SUBJECT     IS TABLE OF PMT_APPOINTMENT.APPOINTMENT_SUBJECT%TYPE        INDEX BY BINARY_INTEGER;  -- 予定内容
TYPE IX_A_APPOINTMENT_START       IS TABLE OF PMT_APPOINTMENT.APPOINTMENT_START%TYPE          INDEX BY BINARY_INTEGER;  -- 予定開始時間
TYPE IX_A_APPOINTMENT_END         IS TABLE OF PMT_APPOINTMENT.APPOINTMENT_END%TYPE            INDEX BY BINARY_INTEGER;  -- 予定終了時間
TYPE IX_A_APPOINTMENT_PROPERTIES  IS TABLE OF PMT_APPOINTMENT.APPOINTMENT_PROPERTIES%TYPE     INDEX BY BINARY_INTEGER;  -- 予定属性
TYPE IX_A_APPOINTMENT_BODY        IS TABLE OF PMT_APPOINTMENT.APPOINTMENT_BODY%TYPE           INDEX BY BINARY_INTEGER;  -- 予定備考
TYPE IX_A_DATA_DIV                IS TABLE OF PMT_APPOINTMENT.DATA_DIV%TYPE                   INDEX BY BINARY_INTEGER;  -- データ区分


-- 受注予定情報登録
PROCEDURE ENTRY
(
	-- 予定情報
	inF_REC_ORDER_SEQ_NO        IN PMT_APPOINTMENT.REC_ORDER_SEQ_NO%TYPE,   -- 受注管理番号
	inF_APPOINTMENT_ID          IN IX_A_APPOINTMENT_ID,                     -- 予定ID
	inF_APPOINTMENT_TYPE        IN IX_A_APPOINTMENT_TYPE,                   -- 予定種類
 	inF_APPOINTMENT_SUBJECT     IN IX_A_APPOINTMENT_SUBJECT,                -- 予定内容
	inF_APPOINTMENT_START       IN IX_A_APPOINTMENT_START,                  -- 予定開始時間
	inF_APPOINTMENT_END         IN IX_A_APPOINTMENT_END,                    -- 予定終了時間
	inF_APPOINTMENT_PROPERTIES  IN IX_A_APPOINTMENT_PROPERTIES,             -- 予定属性
	inF_APPOINTMENT_BODY        IN IX_A_APPOINTMENT_BODY,                   -- 予定備考
	inF_DATA_DIV                IN IX_A_DATA_DIV,                           -- データ区分
	inLAST_DEPT_CD		    IN PMT_ESTIMATE.LAST_DEPT_CD%TYPE,		-- 最終更新者部門CD
	inLAST_USER_CD		    IN PMT_ESTIMATE.LAST_USER_CD%TYPE,		-- 最終更新者CD
	inAPPOINTMENT_COUNT	    IN  NUMBER,				        -- 予定件数
	outSQLCODE	            OUT NUMBER					-- SQLコード
);

END;
